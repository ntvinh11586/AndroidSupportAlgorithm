package com.ntvinh11586.android_support_algorithm;

public class BasicIntegerCalculation {

    public static Integer addTwoIntegers(Integer a, Integer b) {
        return a + b;
    }

    public static Integer subtractTwoIntegers(Integer a, Integer b) {
        return a - b;
    }

    public static Integer multiplyTwoIntegers(Integer a, Integer b) {
        return a * b;
    }

    public static Integer divideTwoIntegers(Integer a, Integer b) {
        return a / b;
    }
}
